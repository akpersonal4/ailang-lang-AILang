def test_json_structure() -> bool:
    """Test: JSON report has correct structure."""
    print("\nTEST 3: JSON report structure...")
    root = Path(__file__).resolve().parent.parent
    json_path = root / "generated" / "TEST_GENERATION_REPORT.json"
    with open(json_path) as f:
        report = json.load(f)
    checks = [
        ("metadata" in report, "metadata section present"),
        ("tool" in report.get("metadata", {}), "metadata.tool present"),
        ("summary" in report, "summary section present"),
        ("tests" in report, "tests array present"),
        ("coverage" in report, "coverage section present"),
        (isinstance(report["tests"], list), "tests is list"),
        (report["summary"]["apps_discovered"] > 0, "apps_discovered > 0"),
    ]
    all_pass = all(result for result, _ in checks)
    if all_pass:
        print("  [PASS] All 7 structure checks passed")
        print("  Apps discovered: %d" % report["summary"]["apps_discovered"])
        return True
    else:
        for result, label in checks:
            if not result:
                print("  [FAIL] %s" % label)
        return False


def test_generates_test_files() -> bool:
    """Test: generates test files in tests/generated/."""
    print("\nTEST 4: Generates test files...")
    # Clean and regenerate
    clean_generated_tests()
    code, out, err = run_command(
        [
            sys.executable,
            "-m",
            "tools.ail_testgen",
            "--force",
            "--quiet",
        ]
    )
    root = Path(__file__).resolve().parent.parent
    gen_dir = root / "tests" / "generated"
    py_files = list(gen_dir.glob("*.py"))
    init_file = gen_dir / "__init__.py"

    checks = [
        (code == 0, "Exit code 0"),
        (len(py_files) > 0, "At least one test file generated"),
        (init_file.exists(), "__init__.py created"),
    ]
    all_pass = all(c[0] for c in checks)
    if all_pass:
        print("  [PASS] %d test files generated" % len(py_files))
        print("  __init__.py: present")
        return True
    else:
        for ok, label in checks:
            if not ok:
                print("  [FAIL] %s" % label)
        return False


def test_generated_file_has_header() -> bool:
    """Test: generated files have the AUTO-GENERATED header."""
    print("\nTEST 5: Generated file header...")
    root = Path(__file__).resolve().parent.parent
    gen_dir = root / "tests" / "generated"
    py_files = sorted(f for f in gen_dir.glob("*.py") if f.name != "__init__.py")
    if not py_files:
        print("  [FAIL] No generated test files found")
        return False
    sample = py_files[0]
    content = sample.read_text(encoding="utf-8")
    if "AUTO-GENERATED BY AILang DX-005" in content:
        print("  [PASS] Header present in %s" % sample.name)
        return True
    else:
        print("  [FAIL] Missing header in %s" % sample.name)
        return False


