# DX Tool #005 Acceptance Testing
# Comprehensive test suite for ail testgen tool

from __future__ import annotations

import hashlib
import json
import shutil
import subprocess
import sys
from pathlib import Path


def run_command(cmd: list[str]) -> tuple[int, str, str]:
    """Run a command and return exit code, stdout, stderr."""
    result = subprocess.run(cmd, capture_output=True, text=True)
    return result.returncode, result.stdout, result.stderr


def hash_file(path: Path) -> str:
    """Get SHA-256 hash of file content."""
    return hashlib.sha256(path.read_bytes()).hexdigest()


def clean_generated_tests():
    """Remove generated test outputs between tests."""
    root = Path(__file__).resolve().parent.parent
    gen_dir = root / "tests" / "generated"
    if gen_dir.exists():
        for f in list(gen_dir.glob("*")):
            try:
                if f.is_file():
                    f.unlink()
                elif f.is_dir():
                    shutil.rmtree(f)
            except PermissionError:
                pass
        try:
            gen_dir.rmdir()
        except PermissionError:
            pass


def test_tool_runs_successfully() -> bool:
    """Test: python -m tools.ail_testgen runs successfully."""
    print("TEST 1: Tool runs successfully (dry run)...")
    code, out, err = run_command(
        [
            sys.executable,
            "-m",
            "tools.ail_testgen",
            "--dry-run",
            "--quiet",
        ]
    )
    if code == 0:
        print("  [PASS] Tool exits with code 0")
        return True
    else:
        print("  [FAIL] Exit code %d" % code)
        print("  Stderr: %s" % err[:500])
        return False


