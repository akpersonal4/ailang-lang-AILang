# AILang Benchmark Runner: Reporter
# Generates Markdown and JSON benchmark reports

from __future__ import annotations

import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def _build_json_payload(
    results: list[dict],
    suite: str,
    threshold_pct: float,
    repeat: int,
    memory: bool,
    timeout: int,
    baseline_name: str | None,
) -> dict[str, Any]:
    """Build the complete JSON report payload."""
    total = len(results)
    passed = sum(1 for r in results if r.get("status") == "pass")
    failed = sum(1 for r in results if r.get("status") == "fail")
    regressed = sum(
        1 for r in results if r.get("regression", {}).get("detected")
    )
    skipped = sum(1 for r in results if r.get("status") == "skip")
    total_time = sum(
        r.get("stats", {}).get("build_time", {}).get("avg", 0)
        for r in results
        if r.get("status") == "pass"
    ) + sum(
        r.get("stats", {}).get("run_time", {}).get("avg", 0)
        for r in results
        if r.get("status") == "pass"
    )

    return {
        "tool": "ail_benchmark",
        "version": "0.1.0",
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "suite": suite,
        "threshold_pct": threshold_pct,
        "config": {
            "repeat": repeat,
            "memory": memory,
            "timeout": timeout,
        },
        "baseline": baseline_name,
        "summary": {
            "total": total,
            "passed": passed,
            "failed": failed,
            "regressed": regressed,
            "skipped": skipped,
            "total_time_seconds": round(total_time, 3),
        },
        "benchmarks": results,
    }


def generate_json_report(
    results: list[dict],
    suite: str,
    threshold_pct: float,
    repeat: int,
    memory: bool,
    timeout: int,
    baseline_name: str | None = None,
) -> str:
    """Generate a JSON benchmark report string."""
    payload = _build_json_payload(
        results, suite, threshold_pct, repeat, memory, timeout, baseline_name
    )
    return json.dumps(payload, indent=2) + "\n"


def _format_stats(stats: dict) -> str:
    """Format benchmark statistics for markdown display."""
    parts = []
    if stats:
        parts.append(f"min {stats.get('min', 0):.3f}s")
        parts.append(f"avg {stats.get('avg', 0):.3f}s")
        parts.append(f"max {stats.get('max', 0):.3f}s")
    return "  \n".join(parts) if parts else "—"


def _format_regression(regression: dict | None, threshold_pct: float) -> str:
    """Format regression info for markdown display."""
    if not regression:
        return "—"
    if not regression.get("detected"):
        return "✓ OK"
    parts = []
    bp = regression.get("build_regression_pct")
    rp = regression.get("run_regression_pct")
    if bp and bp > threshold_pct:
        parts.append(f"build +{bp}%")
    if rp and rp > threshold_pct:
        parts.append(f"run +{rp}%")
    return "⚠ " + ", ".join(parts) if parts else "✓ OK"


def generate_markdown_report(
    results: list[dict],
    suite: str,
    threshold_pct: float,
    repeat: int,
    memory: bool,
    timeout: int,
    elapsed: float,
    baseline_name: str | None = None,
) -> str:
    """Generate a Markdown benchmark report string."""
    total = len(results)
    passed = sum(1 for r in results if r.get("status") == "pass")
    failed = sum(1 for r in results if r.get("status") == "fail")
    regressed = sum(
        1 for r in results if r.get("regression", {}).get("detected")
    )
    skipped = sum(1 for r in results if r.get("status") == "skip")
    total_time = sum(
        r.get("stats", {}).get("build_time", {}).get("avg", 0)
        for r in results
        if r.get("status") == "pass"
    ) + sum(
        r.get("stats", {}).get("run_time", {}).get("avg", 0)
        for r in results
        if r.get("status") == "pass"
    )

    lines = [
        "# AILang Benchmark Report",
        "",
        "_Auto-generated by `ail benchmark` tool_",
        "",
        "## Summary",
        "",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Suite | `{suite}` |",
        f"| Repeat | {repeat}× |",
        f"| Threshold | {threshold_pct}% |",
        f"| Memory | {'Yes' if memory else 'No'} |",
        f"| Timeout | {timeout}s |",
        f"| Total benchmarks | {total} |",
        f"| Passed | {passed} |",
        f"| Failed | {failed} |",
        f"| Regressed | {regressed} |",
        f"| Skipped | {skipped} |",
        f"| Total wall time | {elapsed:.1f}s |",
        f"| Total benchmark time | {total_time:.1f}s |",
        "",
    ]

    if baseline_name:
        lines.append(f"| Baseline | `{baseline_name}` |")
        lines.append("")

    # Per-benchmark details
    lines.extend([
        "## Benchmarks",
        "",
        "| Benchmark | Status | Build (avg) | Run (avg) | Regression | Memory |",
        "|-----------|--------|-------------|-----------|------------|--------|",
    ])

    for r in results:
        name = r["name"]
        status = r.get("status", "?")
        build_stats = r.get("stats", {}).get("build_time", {})
        run_stats = r.get("stats", {}).get("run_time", {})
        build_avg = f"{build_stats.get('avg', 0):.3f}s" if build_stats else "—"
        run_avg = f"{run_stats.get('avg', 0):.3f}s" if run_stats else "—"
        reg = _format_regression(r.get("regression"), threshold_pct)
        mem = f"{r.get('memory_kb', 0):.0f} KB" if r.get("memory_kb") else "—"

        status_symbol = "✓" if status == "pass" else "✗" if status == "fail" else "—"
        status_display = f"{status_symbol} {status}"

        lines.append(
            f"| {name} | {status_display} | {build_avg} | {run_avg} | {reg} | {mem} |"
        )

    lines.append("")

    # Error details for failed benchmarks
    failed_benchmarks = [r for r in results if r.get("status") == "fail"]
    if failed_benchmarks:
        lines.append("## Failures")
        lines.append("")
        for r in failed_benchmarks:
            lines.append(f"### {r['name']}")
            lines.append("")
            lines.append(f"- **Path:** `{r.get('path', '?')}`")
            lines.append(f"- **Error:** {r.get('error', 'Unknown')}")
            lines.append("")

    # Regression details
    regressed_benchmarks = [
        r for r in results if r.get("regression", {}).get("detected")
    ]
    if regressed_benchmarks:
        lines.append("## Regressions")
        lines.append("")
        for r in regressed_benchmarks:
            reg = r.get("regression", {})
            lines.append(f"### {r['name']}")
            lines.append("")
            bp = reg.get("build_regression_pct")
            rp = reg.get("run_regression_pct")
            if bp:
                lines.append(f"- Build: +{bp}% (threshold: {threshold_pct}%)")
            if rp:
                lines.append(f"- Run: +{rp}% (threshold: {threshold_pct}%)")
            lines.append("")

    # Run details for passed benchmarks
    lines.append("## Run Details")
    lines.append("")
    for r in results:
        if r.get("status") != "pass":
            continue
        lines.append(f"### {r['name']}")
        lines.append("")
        build_stats = r.get("stats", {}).get("build_time", {})
        run_stats = r.get("stats", {}).get("run_time", {})
        memory_kb = r.get("memory_kb")
        lines.append("| Metric | Build | Run |")
        lines.append("|--------|-------|-----|")
        lines.append(
            f"| Min | {build_stats.get('min', 0):.3f}s | "
            f"{run_stats.get('min', 0):.3f}s |"
        )
        lines.append(
            f"| Avg | {build_stats.get('avg', 0):.3f}s | "
            f"{run_stats.get('avg', 0):.3f}s |"
        )
        lines.append(
            f"| Max | {build_stats.get('max', 0):.3f}s | "
            f"{run_stats.get('max', 0):.3f}s |"
        )
        lines.append(
            f"| Median | {build_stats.get('median', 0):.3f}s | "
            f"{run_stats.get('median', 0):.3f}s |"
        )
        if memory_kb is not None:
            lines.append(f"| Peak memory | — | {memory_kb:.0f} KB |")
        lines.append("")

    lines.extend([
        "---",
        "_This report was generated by the `ail benchmark` tool._",
        "",
    ])

    return "\n".join(lines)
